def load_date(fake):
    """Load / generate some fake dates using rules for our date translation system.

    Parameters
    ----------
    fake : Faker() object
        A faker object from the fake package that can be used to generate fake date time objects.

    Returns
    -------
    (human_readable, machine_readable, date) : tuple (str, str, date_object)
        Returns a tuple containing a human readable string, a machine readable string
        and the actual machine datetime object these represent.
    """
    date = fake.date_object()

    try:
        human_readable = format_date(date, format=random.choice(FORMATS),  locale='en_US') # locale=random.choice(LOCALES))
        human_readable = human_readable.lower()
        human_readable = human_readable.replace(',','')
        machine_readable = date.isoformat()
        
    except AttributeError as e:
        return None, None, None

    return human_readable, machine_readable, date


def load_dataset(num_samples):
    """ "Loads" e.g. generates a dataset with num_samples examples.  A
    random dataset is generated by this function using some rules.

    Parameters
    ----------
    num_samples : int
        The size of the dataset to load / generate.

    Returns
    -------
    text_pairs : list of tuples (str, str)
        The dataset_pairs is a regular list of pairs of strings, the human and machine
        readable date strings respectively.
    """
    # the faker package is useful for generating fake data.  Set seeds here so this method
    # always returns the same fake generated dataset
    fake = Faker()
    Faker.seed(12345)
    random.seed(12345)

    text_pairs = []    

    # generate asked for number of samples, each call to load_date()
    # generates 1 sample
    for sample in tqdm(range(num_samples)):
        human_readable, machine_readable, _ = load_date(fake)
        if human_readable is not None:
            text_pairs.append((human_readable, 's' + machine_readable + 'e'))
    
    return text_pairs


def decode_sequence(model, input_sentence, source_sequence_length, source_vectorizor, target_sequence_length, target_vectorizor):
    """Use the trained encoder/decoder translator to translate the given
    input sentence in English for us.

    Parameters
    ----------
    model : Keras model
        A trained encoder / decoder model on the Date sequence translation task.
    input_sentence : str
        A string of the input human readable date sentence to be translated.
    source_vectorizor, target_vectorizor : TextVectorization
        The vectorizors used for source and targets, need both to first vectorize
        the given input sentence, then to decode the output sequence we get.
    """
    # create inverse lookup so can get an index and find the token from the target vocabulary
    target_vocab = target_vectorizor.get_vocabulary()
    target_index_lookup = dict(zip(range(len(target_vocab)), target_vocab))
    source_vocab_size = source_vectorizor.vocabulary_size()
    target_vocab_size = target_vectorizor.vocabulary_size()

    # the source vectorizor will normalize and tokenize sentence for us.
    tokenized_input_sentence = source_vectorizor([input_sentence])
    tokenized_input_sentence = to_categorical(tokenized_input_sentence, source_vocab_size)
    
    # we will gather the output of the decoder translation in this string, which initially only
    # has the special [start] seed token
    decoded_sentence = "s"
    for i in range(target_sequence_length):
        # basic inference, get prediction of next token from the trained model for the input
        # sentence, and what we have so far in the target translation sentence
        tokenized_target_sentence = target_vectorizor([decoded_sentence])[:,:-1]
        tokenized_target_sentence = to_categorical(tokenized_target_sentence, target_vocab_size)
        inputs = {
            'source_inputs': tokenized_input_sentence,
            'target_inputs': tokenized_target_sentence
        }
        next_token_predictions = model.predict(inputs, verbose=False)

        # convert next token prediction to a string.  argmax gives the token index
        # and use the reverse target vocabulary dictionary to look up the word string
        sampled_token_index = np.argmax(next_token_predictions[0, i, :])
        sampled_token = target_index_lookup[sampled_token_index]

        # we append the next predicted word to end of translated sentence so for
        decoded_sentence += sampled_token

        # we stop if see special [end] token, or loop will stop once we reach our maximum sentence length
        if sampled_token == "e":
            break

    # return the decoded/translated sentence
    return decoded_sentence

