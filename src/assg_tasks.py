class TfStackWrapper(layers.Layer):
    def call(self, x):
        return tf.stack(x, axis=1)
   

def softmax(x, axis=1):
    """Custom softmax activation function, will perform softmax over axis other than last

    Parameters
    ----------
    x : Tensor.
    axis: Integer, axis along which the softmax normalization is applied.

    Returns
    -------
        Tensor, output of softmax transformation.

    Raises
    ------
        ValueError: In case `dim(x) == 1`.
    """
    ndim = K.ndim(x)
    if ndim == 2:
        return K.softmax(x)
    elif ndim > 2:
        e = K.exp(x - K.max(x, axis=axis, keepdims=True))
        s = K.sum(e, axis=axis, keepdims=True)
        return e / s
    else:
        raise ValueError('Cannot apply softmax to a tensor that is 1D')

class EveryNEpochs(keras.callbacks.Callback):
    """Subclass keras Callback to provide a callback to report loss and accuracy
    metrics at the end of every Nth epoch of training

    Attributes
    ----------
    N : int
       The epoch step size, we report metrics at the end of every Nth epoch of training. 
    """
    def __init__(self, N):
        """Chain the superclass constructor, add N attribute for this subclass.

        Parameters
        ----------
        N : int
           The epoch step size, report every N epochs evaluation metrics
        """
        super().__init__()
        self.N = N

    def on_epoch_end(self, epoch, logs={}):
        """Callback member override, called at the end of each epoch.
        We determine if the current epoch is divisible by our
        epoch step N, and if so display the metrics.  This callback
        assumes loss and accuracy are being monitored, and that validation
        data is being assessed during the training loop

        Parameters
        ----------
        epoch : int
           The current epoch number that just ended
        logs  : dict
           A dictionary like object.  The loss and accuracy metrics are assumed to be
           tracked, and further this callback assumes validation data is being used.
           So keys `loss`, `accuracy` are expected for the training data and
           `val_loss`, `val_accuracy` are expected for the validation data.
        """
        last_epoch = self.params['epochs'] - 1
        if epoch % self.N == 0 or epoch == (last_epoch):
            train_loss = logs.get('loss')
            train_accuracy = logs.get('accuracy')
            val_loss = logs.get('val_loss')
            val_accuracy = logs.get('val_accuracy')
            if val_accuracy == None:
                print(f"Epoch: {epoch:04d}/{last_epoch:04d}  training loss - {train_loss:0.4f}  training_accuracy - {train_accuracy:0.4f}")
            else:
                print(f"Epoch: {epoch:04d}/{last_epoch:04d}  training loss - {train_loss:0.4f}  validation loss - {val_loss:0.4f}  training_accuracy - {train_accuracy:0.4f}  validation accuracy - {val_accuracy:0.4f}")


def plot_history(ax, history_dict, metric_key):
    """Plot the asked for metrics. Usually we need to plot the metric from the training
    data and its corresponding measurement using the validation data, thus we pass in
    two keys for the training and validation metric to plot.

    Arguments
    ---------
    ax - a matplotlib figure axis to create plot onto
    history_dict - A Python dictionary whose keys should return list like enumerable
      items holding the measured metrics over some number of epochs of training.
    metric_key - The string key for the metric, validation data is assumed to be
      accessible as "val_" + metric_key


    """
    # setup epochs and keys/labels for the plot
    train_key = metric_key
    train_label = "Training " + metric_key
    val_key = "val_" + metric_key
    val_label = "Validation " + metric_key
    epochs = np.arange(1, len(history_dict[train_key]) + 1)
    
    # create the plot of the train and test metric
    ax.plot(epochs, history_dict[train_key], 'r-', label=train_label)
    ax.plot(epochs, history_dict[val_key], 'b-', label=val_label)
    ax.set_xlabel('Epochs')
    ax.set_ylabel(metric_key)
    #ax.set_xticks(epochs)
    ax.grid()
    ax.legend();

